// AJAX CALL FOR GETTING SELECTED MOVIE
function getMovie(movieId){
	let promise = new Promise((resolve, reject) => {
		ajax.onreadystatechange = () => {
			if(ajax.readyState == 4){
				let movie = JSON.parse(ajax.responseText);
				console.log("movie: " + movie);
				generatePage(movie);
				resolve();
			}
		}
		
		ajax.open("GET", "http://localhost:8082/MovieFandom/MovieDetailsServlet?movie="+movieId);
		ajax.send();
	})
	
	return promise;
}

// GENERATE MOVIE PAGE
function generatePage(movie){
	console.log("movie js : " + movie);
	title.innerText = movie.name;
	generateGenres(movie.tags);
	generateMovieHeader(movie.release_date, movie.duration);
	synopsis.innerText = movie.movie_desc;
	generateCasts(movie.casts);
	generateMovieView(movie.poster_url, movie.trailer_url);
}

